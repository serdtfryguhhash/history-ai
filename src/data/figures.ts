export function getFigureBySlug(slug: string): HistoricalFigure | undefined {
  return historicalFigures.find((f) => f.slug === slug);
}

export function getFiguresByCategory(category: string): HistoricalFigure[] {
  return historicalFigures.filter((f) => f.categories.includes(category));
}

export function searchFigures(query: string): HistoricalFigure[] {
  const lower = query.toLowerCase();
  return historicalFigures.filter(
    (f) =>
      f.name.toLowerCase().includes(lower) ||
      f.title.toLowerCase().includes(lower) ||
      f.era.toLowerCase().includes(lower) ||
      f.short_bio.toLowerCase().includes(lower) ||
      f.categories.some((c) => c.toLowerCase().includes(lower))
  );
}
