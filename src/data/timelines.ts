export function getTimelineByTopic(topic: string): TimelineEvent[] {
  return timelineEvents.filter((e) => e.topic === topic).sort((a, b) => a.year - b.year);
}

export function getTopicBySlug(slug: string) {
  return timelineTopics.find((t) => t.slug === slug);
}
